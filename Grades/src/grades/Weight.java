package grades;

public class Weight {
	
	private int quizW;
	private int projectW;
	private int midW;
	private int finalExamW;
	
	public Weight(int quizW, int projectW, int midW, int finalExamW) {
		super();
		this.quizW = quizW;
		this.projectW = projectW;
		this.midW = midW;
		this.finalExamW = finalExamW;
	}

	public int getQuizW() {
		return quizW;
	}

	public int getProjectW() {
		return projectW;
	}


	public int getMidW() {
		return midW;
	}

	public int getFinalExamW() {
		return finalExamW;
	}

	
	
	
	
	
	

}
